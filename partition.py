def integer_partition(integers):
    """
    The integer partition definition
    :param integers: an array of integers
    :return: "NO" if no solution exists, otherwise, the set of numbers in one of the two subsets in an arbitrary order
    """
    total = sum(integers)
    
    if (total % 2 != 0) :
        return "NO"
        
    k = total / 2
    n = len(integers)
    arr = [[0 for i in range(k+1)] for j in range(n+1)]
    
    for i in range(1, k + 1) :
        arr[0][i] = False
    
    for i in range(n + 1) :
        arr[i][0] = True
    
    for i in range(1, n + 1) :
        for j in range(1, k + 1) :
            arr[i][j] = arr[i - 1][j]
            if (integers[i - 1] <= j):
                arr[i][j] = (arr[i][j] or arr[i - 1][j - integers[i - 1]])
    
    set1 = []
    set2 = []
    
    if (arr[n][k] == False) :
        return "NO"
    
    i = n
    j = k
    
    while (i > 0 and j >= 0) :
        if (arr[i - 1][j]):
            i -= 1
            set2.append(integers[i])
        
        elif (arr[i - 1][j - integers[i - 1]]):
            i -= 1
            j -= integers[i]
            set1.append(integers[i])
    
    set1.sort()
     
    return set1


